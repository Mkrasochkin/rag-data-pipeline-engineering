# Ревью 4.1: Проверка извлечения заголовков разного уровня регулярными выражениями.
# ============================================================================
def test_extract_sections_finds_valid_headers():
    """Тест: регулярка должна четко находить заголовки глав и пунктов"""
    text = "5 ОРГАНИЗАЦИЯ СТРОИТЕЛЬСТВА\nТекст главы.\n5.1 Общие положения"
    
