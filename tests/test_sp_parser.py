# ТЕСТ 8: НАРЕЗКА НА ПУНКТЫ И ТАБЛИЦЫ (split_plain_sp_into_blocks)
# Ревью 4.2: Проверка требований по изоляции таблиц от основного текста.
# ============================================================================
def test_split_plain_sp_into_blocks_with_table(chunker):
    sample_text = (
        "5.1 Организация работ должна соответствовать проекту.\n"
        "Таблица 1 — Параметры площадки\n"
        "| Ширина | Длина |\n"
        "|---|---|\n"
        "| 10 м | 20 м |\n"
        "Конец таблицы."
    )
    blocks = chunker.split_plain_sp_into_blocks(sample_text)
    
    # Скрипт должен разделить этот текст на текстовые части и табличные части
    assert len(blocks) >= 2
    # Проверяем, что исходные куски текста и таблицы выделились
    assert any("Организация работ" in b for b in blocks)
    assert any("| Ширина |" in b for b in blocks)
