# ============================================================================
# ТЕСТ 1: УДАЛЕНИЕ НОМЕРОВ СТРАНИЦ И МУСОРА
# Проверяем базовую чистку служебной информации из Markdown
# ============================================================================
def test_clean_text_removes_page_numbers():
    """Тест: функция должна удалять номера страниц из Markdown"""
    dirty_text = "Текст строительной нормы.\nСтраница 15 из 270\nПродолжение текста."
    expected = "Текст строительной нормы.\nПродолжение текста."
    assert clean_text(dirty_text).strip() == expected.strip()


# ============================================================================
# ТЕСТ 2: ПЕРЕНОСЫ СТРОК (Проверка склейки разорванных слов/предложений)
# Ревью 4.4: Очистка текста от жестких переносов строк внутри предложений.
# ============================================================================
def test_sp_hyphenation_and_newlines():
    sample_text = (
        "Контроль качества осуществ-\nляют в процессе монтажа.\n"
        "Технический надзор\nпроверяет скрытые работы."
    )
    cleaned = clean_text(sample_text)
    
    # Проверяем, что дефис удален, а разорванное слово склеилось
    assert "осуществляют" in cleaned
    # Проверяем, что случайный разрыв строки заменен на пробел
    assert "надзор проверяет" in cleaned


# ============================================================================
# ТЕСТ 3: СОДЕРЖАНИЕ И ВВЕДЕНИЕ (Проверка удаления служебного оглавления)
# Ревью 4.5: Проверка, что блок 'Содержание' отрезается и не идет в чанки.
# ============================================================================
def test_extract_main_content_trims_properly():
    """Тест: функция должна отсекать введение и оставлять суть с Области применения"""
    full_doc = (
        "ПРЕДИСЛОВИЕ\nКакая-то комиссия утвердила СП.\n"
        "СОДЕРЖАНИЕ\n"
        "1 Область применения ................. 3\n"
        "2 Нормативные ссылки ................. 4\n"
        "1 Область применения\n"
        "Настоящий свод правил распространяется на...\n"
        "Приложение А\nТаблица климатических данных"
    )
    result = extract_main_content(full_doc)
    
    assert "ПРЕДИСЛОВИЕ" not in result
    assert "................." not in result
    assert "1 Область применения" in result
    assert "Приложение А" not in result


